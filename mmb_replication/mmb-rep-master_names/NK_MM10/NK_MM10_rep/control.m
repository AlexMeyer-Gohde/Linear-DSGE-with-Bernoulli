for i = 1:TT
   netr(i) = 100*(((IR(i,iRd)/100+1.0)*Rd_ss)^4-1);
   netrB(i) = 100*(((IRB(i,iRd)/100+1.0)*Rd_ss)^4-1);
   infl(i) = 100*(((IR(i,iinfl)/100+1.0)*pi_ss)^4-1.0);
   inflB(i) = 100*(((IRB(i,iinfl)/100+1.0)*pi_ss)^4-1.0);
end;





